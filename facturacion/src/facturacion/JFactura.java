    private void agregarproductobotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarproductobotonActionPerformed
        tablaproductos.setModel(modelo);
        String product = codproductotf.getText();
        int cantidad = Integer.parseInt(String.valueOf(spinercantidad.getValue()));

        if (obtener_datos(product) && currentproduct != null && currentproduct.ComprobarProducto(cantidad)) {
            Object[] rowdata = {tablaproductos.getRowCount() + 1, currentproduct.getNumeroproducto(), currentproduct.getDescripcion(), currentproduct.getProPrecio(), currentproduct.getIva(), currentproduct.sumador(currentproduct.getProPrecio(), currentproduct.getIva())};
            modelo.addRow(rowdata);

            acumulador = acumulador + currentproduct.sumador(currentproduct.getProPrecio(), currentproduct.getIva());
            acumuladoriva = acumuladoriva + currentproduct.getIva();
            acumuladorbruto = acumuladorbruto + currentproduct.getProPrecio();

            totaltextfield.setText("" + acumulador);

        } else {
            JOptionPane.showMessageDialog(null, "No se encontró el producto o el cliente: \n" + product);
        }
    }//GEN-LAST:event_agregarproductobotonActionPerformed

