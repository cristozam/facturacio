public boolean obtener_datos(String codigo) {
    currentproduct = new Producto(codigo); // Asegúrate de inicializar currentproduct aquí

    String pronum = null; 
    String descripcion = null;
    int cantidad = 0;
    double precio = 0;
    double iva = 0.0;

    boolean bandera = false;

    String sql = "SELECT PROCODIGO, PRODESCRIPCION, PROSALDOFINAL, PROPRECIOUM, PROIVA FROM productos WHERE PROCODIGO = ?";

    try (Connection conn = conexionBD.getConnection();
         PreparedStatement pstmt = conn.prepareStatement(sql)) {

        pstmt.setString(1, codigo);
        ResultSet rs = pstmt.executeQuery();

        if (rs.next()) {
            pronum = rs.getString("PROCODIGO");
            descripcion = rs.getString("PRODESCRIPCION");
            cantidad = rs.getInt("PROSALDOFINAL");
            precio = rs.getDouble("PROPRECIOUM");
            iva = rs.getDouble("PROIVA");

            currentproduct.setDescripcion(descripcion);
            currentproduct.setIva(iva);
            currentproduct.setProPrecio(precio);
            currentproduct.setProsaldofinal(cantidad);
            bandera = true;
        } else {
            System.out.println("No se encontró el producto con el código: " + codigo);
        }

    } catch (SQLException e) {
        e.printStackTrace();
    }

    return bandera;
}



    

